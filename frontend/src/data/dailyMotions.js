// Get today's motion by IST date
export function getDailyMotion(isJunior = false) {
  const now = new Date();
  const ist = new Date(now.getTime() + 5.5 * 60 * 60 * 1000);
  const start = new Date(ist.getFullYear(), 0, 0);
  const diff = ist - start;
  const dayOfYear = Math.floor(diff / (1000 * 60 * 60 * 24));

  const motions = isJunior ? JUNIOR_MOTIONS : SENIOR_MOTIONS;
  return motions[dayOfYear % motions.length];
}

// Get IST date string for today
export function getISTDateString() {
  const now = new Date();
  const ist = new Date(now.getTime() + 5.5 * 60 * 60 * 1000);
  return ist.toISOString().split('T')[0];
}

// Milliseconds until midnight IST
export function msUntilISTMidnight() {
  const now = new Date();
  const ist = new Date(now.getTime() + 5.5 * 60 * 60 * 1000);
  const midnight = new Date(ist);
  midnight.setHours(24, 0, 0, 0);
  return midnight - ist;
}
